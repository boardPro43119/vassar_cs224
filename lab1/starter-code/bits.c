/* 
 * tmin - return minimum two's complement integer 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 4
 *   Rating: 1
 */
int tmin(void) {
  /* Min 2's complement int has MSB 1, all other bits 0 */
  return (1 << 31);
}
/* 
 * TMax - return maximum two's complement integer 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 4
 *   Rating: 1
 */
int tmax(void) {
  /* Max 2's complement int has MSB 0, all other bits 1 */
  return ~(1 << 31);
}
/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
  /* Check if bits are not the same in x and y */
  return (~(x & y) & ~(~x & ~y));
}
/* 
 * bitAnd - x&y using only ~ and | 
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
int bitAnd(int x, int y) {
  /* Flip all bits of x and y, use or to check which are both 0 (which are not both 1 in original x and y), then flip back */
  return ~(~x | ~y);
}
/*
 * isZero - returns 1 if x == 0, and 0 otherwise 
 *   Examples: isZero(5) = 0, isZero(0) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 2
 *   Rating: 1
 */
int isZero(int x) {
  /* Use unary ! operator to get 1 if x is nonzero, 0 otherwise */ 
  return !x;
}
/* 
 * minusOne - return a value of -1 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 2
 *   Rating: 1
 */
int minusOne(void) {
  /* -1 in binary 2's complement is all 1's */
  return ~0;
}
/* 
 * thirdBits - return word with every third bit (starting from the LSB) set to 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 1
 */
int thirdBits(void) {
 /* Shift bits as far as possible to the left at a time so we can correct the less significant zero bits by adding an 8-bit constant */
  int result = 1;
  result = (result << 9) + 73;
  result = (result << 9) + 73;
  result = (result << 9) + 73;
  result = (result << 3) + 1;
  return result;
}
/* 
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
  /* Set most significant bit to least significant bit of x, then use arithmetic right shift to set all bits equal to it */
  return ((x << 31) >> 31);
}
/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
  /* Take bitwise not and add one */
  return ~x+1;
}
/* 
 * oddBits - return word with all odd-numbered bits set to 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 2
 */
int oddBits(void) {
  /* Start by setting 8 least significant bits to correct values, then shift them left by 8 at a time and correct least significant bits each time */
  int result = 170;
  result = (result << 8) + 170;
  result = (result << 8) + 170;
  result = (result << 8) + 170;
  return result;
}
/* 
 * sign - return 1 if positive, 0 if zero, and -1 if negative
 *  Examples: sign(130) = 1
 *            sign(-23) = -1
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 10
 *  Rating: 2s
 */
int sign(int x) {
    /* Save two variables copying x and -x's MSB's. These will equal 0 if the number is positive and -1 if it is negative */
    int y = (x >> 31);
    int z = ((~x + 1) >> 31);
  
    /* If x is zero, then y and z are both 0, we should return 0 */ 
    /* If x is positive, then y==0 and z==-1, we should return 1 */
    /* If x is negative, then y==-1 and z==0, we should return -1 */
   
    /* The formula y-z gives us the value we need */  
    return y + (~z + 1);
}
/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
    int a = n << 3; /* = number of bytes to shift right to extract nth byte, equal to 8*n */
    int b = m << 3; /* = number of bytes to shift right to extract mth byte, equal to 8*m */
    /* Extract nth and mth bytes by shifting them right and clearing all other bits */
    int nthByte = (x >> a) & 255;
    int mthByte = (x >> b) & 255;
    /* Mask for other two bytes */
    int complement = ~((255 << a) | (255 << b));
    /* Swap the nth and mth bytes, or result with complement to preserve other two bytes */
    return ((nthByte << b) | (mthByte << a)) | (x & complement);
}
/* 
 * bitMask - Generate a mask consisting of all 1's 
 *   between lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int bitMask(int highbit, int lowbit) {
  /* Int with all bits up to highbit equal to 1 */
  int y = (1 << highbit << 1) + ~0;
  /* Int with all bits down to lowbit equal to 1 */
  int z = ~0 << lowbit;

  /* Return overlapping 1 bits */
  return (y&z);
}
/* 
 * reverseBytes - reverse the bytes of x
 *   Example: reverseBytes(0x01020304) = 0x04030201
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 25
 *   Rating: 3
 */
int reverseBytes(int x) {
  /* Extract individual bytes by right shifting and masking, then reverse them by left shifting in reverse order */
  int byte0 = x & 255;
  int byte1 = (x >> 8) & 255;
  int byte2 = (x >> 16) & 255;
  int byte3 = (x >> 24) & 255;
  return (((((byte0 << 8) + byte1) << 8) + byte2) << 8) + byte3;
}
/*
 * fitsBits - return 1 if x can be represented as an
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  /* Copy the value of bit n-1 to all more significant bits by left shifting and then right shifting by 32-n bits*/ 
  int r = 32 + (~n + 1);
  int y = (x << r) >> r; 

  /* For x-values that work, left then right shifting will leave x unchanged. Use xor and unary not to return 1 if y is equal to x (no bits are different) */
  return !(y ^ x);
}
